     public void setStorage(Tables tablaseleccion, vista vista){
        this.vista=vista;
        this.tablaseleccion =tablaseleccion;
        row = new Rows<>(tablaseleccion);
        ColumnCursors columnas = (ColumnCursors)tablaseleccion.getColumns(DataStructure.DOUBLYLINKEDLIST);
        boolean bandera=true;
        while(bandera){
            arreglo.add(columnas.getElement().getMetaData().getName());
            bandera=columnas.next();
        }
        jLabel4.setText((String)arreglo.get(catidadColumnas));
        
    }

