    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if("".equals(jTextField1.getText())){
            JOptionPane.showMessageDialog(frame,"por favor ingre un dato");
            return;
        }
        if(count<arreglo.size()){
        if(catidadColumnas!=arreglo.size()-1){
            ++catidadColumnas;
        }
        jLabel4.setText((String)arreglo.get(catidadColumnas));
        row.appentElement(jTextField1.getText());
        jTextField1.setText("");
        count++;
        
        }
        else{
            JOptionPane.showMessageDialog(frame,"La fila ya esta completa");
        }
        
        
        //.addRow(row);
    }//GEN-LAST:event_jButton1ActionPerformed

